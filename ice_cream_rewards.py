def get_reward(rank_num: int):
    available = [f for f in FLAVORS.values() if f[2] <= rank_num]
    if not available:
        available = list(FLAVORS.values())[:5]
    flavor = random.choice(available)
    return f"{flavor[0]}\n🏹 {flavor[1]}"

def get_all_available(rank_num: int):
    return [f for f in FLAVORS.values() if f[2] <= rank_num]

def get_flavor_by_name(name: str):
    clean = name.lower().strip()
    for f in FLAVORS.values():
        short = f[0].split(" ", 1)[1].lower()
        if short == clean:
            return f
    return None

def get_total_count():
    return len(FLAVORS)
