def get_cipher(flavor_name: str, user_rank: int):
    key = flavor_name.lower().strip()
    entry = CIPHER.get(key)
    if not entry:
        return None, "Такого вкуса нет в Леднике Шамана."
    if user_rank < entry["min_rank"]:
        return None, f"Шаман не раскрывает тайну «{key}» до ранга {entry['min_rank']}."
    return entry["shaman"], None
